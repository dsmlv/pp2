#upper case of string
a = "Hello, World!"
print(a.upper())

#lower
a = "Hello, World!"
print(a.lower())

#remove whitespace
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"

#replace
a = "Hello, World!"
print(a.replace("H", "J"))

#split
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']